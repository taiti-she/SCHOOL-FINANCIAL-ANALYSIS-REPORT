SELECT 
    AVG(TOTAL_FEES) AS FEE_PER_STUDENT
FROM FEES;
-- AVERAGE FEES PER STUDENT



WITH FIXEDCOSTS AS (
    SELECT SUM(AMOUNT) AS FIXED_COSTS
    FROM EXPENSES
     WHERE DESCRIPTION IN ('Building & Construction', 'Salaries & Wages', 'Rent Expenses', 'Loans', 'Utilities', 'Miscallenous')
),
VARIABLEPERSTUDENT AS (
    SELECT SUM(AMOUNT) / 220 AS VAR_PER_STUDENT
    FROM EXPENSES
    WHERE DESCRIPTION IN ('Kitchen Expenses', 'Transport Expenses', 'Learning Materials', 'Maintenance Supplies')
),
FEESPERSTUDENT AS (
    SELECT AVG(TOTAL_FEES) AS FEE
    FROM FEES
)

SELECT 
    FIXED_COSTS / (FEE - VAR_PER_STUDENT) AS BREAK_EVEN_STUDENTS
FROM FIXEDCOSTS, VARIABLEPERSTUDENT, FEESPERSTUDENT;
-- BREAK-EVEN STUDENTS