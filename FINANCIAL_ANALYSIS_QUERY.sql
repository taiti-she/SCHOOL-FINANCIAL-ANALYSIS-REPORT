WITH MONTHLYEXPENSES AS (
    SELECT 
        MONTH([DATE]) AS MONTH,
        SUM(AMOUNT) AS TOTAL_EXPENSES
    FROM EXPENSES
    GROUP BY MONTH([DATE])
)

SELECT *
FROM MONTHLYEXPENSES
WHERE TOTAL_EXPENSES > 250000
ORDER BY TOTAL_EXPENSES DESC;
