SELECT 
SUM(TOTAL_FEES) AS TOTAL_EXPECTED,
SUM(AMOUNT_PAID) AS TOTAL_COLLECTED,
SUM(BALANCE_OUTSTANDING) AS TOTAL_OUTSTANDNG
FROM FEES;
--TOTAL REVENUE VS COLLECTED REVENUE



SELECT GRADE, SUM(AMOUNT_PAID) AS REVENUE
FROM FEES
GROUP BY GRADE 
ORDER BY REVENUE DESC;
-- REVENUE BY GRADE



SELECT GRADE, SUM(BALANCE_OUTSTANDING) AS OUTSTANDING
FROM FEES
GROUP BY GRADE ORDER BY OUTSTANDING DESC;
--OUTSTANDING FEES BY GRADE


SELECT TOP 10 STUDENT_NAME, BALANCE_OUTSTANDING FROM FEES
ORDER BY BALANCE_OUTSTANDING DESC;
-- TOP DEFAULTERS


SELECT SUM(BALANCE_OUTSTANDING) AS TOTAL_TOP10_BALANCE
FROM(
	SELECT TOP 10 STUDENT_NAME, BALANCE_OUTSTANDING FROM FEES
	ORDER BY BALANCE_OUTSTANDING DESC
) AS TOP10;
--TOP DEFAULTERS SUM



SELECT SUM(AMOUNT) AS TOTAL_EXPENDITURE
FROM EXPENSES
--TOTAL EXPENDITURE


SELECT CATEGORY, SUM(AMOUNT) AS TOTAL_SPENT
FROM EXPENSES
GROUP BY CATEGORY
ORDER BY TOTAL_SPENT DESC
-- EXPENDITURE BY CATEGORY


SELECT DATENAME(MONTH,[DATE]) AS MONTH_NAME, SUM(AMOUNT) AS TOTAL
FROM EXPENSES
GROUP BY DATENAME(MONTH,[DATE]), MONTH([DATE])
ORDER BY MONTH([DATE])
---TOTAL EXPENDITURE BY MONTH


SELECT 
	(SELECT SUM(AMOUNT_PAID) FROM FEES) AS TOTAL_COLLECTED,
	(SELECT SUM(AMOUNT) FROM EXPENSES) AS TOTAL_EXPENSES,
	(SELECT SUM(AMOUNT_PAID) FROM FEES) - (SELECT SUM(AMOUNT) FROM EXPENSES) AS PROFIT
--PROFIT

SELECT 
	TOTAL_COLLECTED,
	TOTAL_EXPENSES,
	PROFIT,
	(PROFIT * 1.0/TOTAL_COLLECTED)* 100 AS PROFIT_MARGIN_PERCENT
FROM (
	SELECT
		(SELECT SUM(AMOUNT_PAID) FROM FEES) AS TOTAL_COLLECTED,
		(SELECT SUM(AMOUNT) FROM EXPENSES) AS TOTAL_EXPENSES,
		(SELECT SUM(AMOUNT_PAID) FROM FEES) - (SELECT SUM(AMOUNT) FROM EXPENSES) AS PROFIT
	) T;
-- PROFIT MARGIN


