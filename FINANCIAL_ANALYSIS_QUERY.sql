WITH DAILYEXPENSES AS (
    SELECT 
        [DATE],
        SUM(AMOUNT) AS DAILY_TOTAL
    FROM EXPENSES
    GROUP BY [DATE]
)

SELECT 
    [DATE],
    DAILY_TOTAL,
    SUM(DAILY_TOTAL) OVER (ORDER BY [DATE]) AS RUNNING_TOTAL
FROM DAILYEXPENSES;
--ACCUMULATION OVER TIME



